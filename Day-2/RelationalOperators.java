public class RelationalOperators {
    public static void main(String[] args) {
        int num1=30;
        System.out.println(num1);
        int num2=60;
        System.out.println(num1==num2);
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1!=num2);

    }
}
