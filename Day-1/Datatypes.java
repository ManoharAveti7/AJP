public class Datatypes{
    public static void main(String args[]){
        byte myByteNum=100;
        System.out.println("byte= "+myByteNum);
        float myFloatNum=23.9f;
        System.out.println("float= "+myFloatNum);
        double myDoubleNum=9988.99d;
        System.out.println("float= "+myDoubleNum);
        boolean myBooleanNum=true;
        System.out.println("boolean= "+myBooleanNum);
        String myString="Goo";
        System.out.println("string= "+myString);



    }
}